var grade = 'A';
switch (grade) {
    case 'A':
        console.log("Excellent job!");
        break;
    case 'B':
        console.log("Good job!");
        break;
    case 'C':
        console.log("Pretty good!");
        break;
    case 'D':
        console.log("Not too good.");
        break;
    case 'F':
        console.log("Failed!");
        break;
    default:
        console.log("Invalid grade.");
        break;
}
